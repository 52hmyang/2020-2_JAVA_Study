public class UpgradeFor {

	public static void main(String[] args) {

		String[] strArr = {"Java", "Android", "C"};
		
		for(String s : strArr) {
			System.out.println(s);
		}
		
		int[] arr = {1, 2, 3, 4, 5};
		for(int num : arr) {	// num = arr[i]
			System.out.println(num);
		}
	}

}
